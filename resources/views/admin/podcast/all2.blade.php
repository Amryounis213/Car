     <script type="text/javascript">
        $(document).ready(function () {
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            const oTable = $('#kt_table_users').DataTable();
            $(document).on('click', ".del_rec_btn", function (e) {
                e.preventDefault();
                const id = $(this).data('id');
                let url = "{{ route('admin-delete-podcast', ":id") }}";
                url = url.replace(':id', id);

                Swal.fire({
                    title: "{{ __('dashboard.warning') }}",
                    text: "{{ __('dashboard.are_you_sure_you_want_to_delete_data') }}",
                    icon: 'warning',
                    confirmButtonText: "{{ __('dashboard.yes_delete') }}",
                    confirmButtonColor: '#d33',
                    cancelButtonText: "{{ __('dashboard.no_cancel') }}",
                    showCancelButton: true,
                }).then((result) => {
                    if (result.value) {
                        $.ajax({
                            type: "DELETE",
                            url: url,
                            success: function (data) {
                                oTable.draw();
                                if(data.success == 'false')
                                {
                                    Swal.fire({
                                        title: 'Error!',
                                        text: 'لا يمكن حذف هذا البودكاست لوجود حلقات مرتبطة به',
                                        icon: 'error',
                                        confirmButtonText: 'حسناً',
                                        confirmButtonColor: '#d33',
                                    })
                              
                               
                                }else
                                {
                                    Swal.fire({
                                        title: 'تم!',
                                        text: 'تم حذف البيانات بنجاح',
                                        icon: 'success',
                                        confirmButtonText: 'حسناً',
                                        confirmButtonColor: '#d33',
                                    })
                                }

                            }
                        });
                    }
                })
            });
        });
    </script>
     <script>
        $(document).ready(function () {
            let oTable = $('#kt_table_users').DataTable();
            oTable.on( 'order.dt search.dt', function () {
                oTable.column(0, {search:'applied', order:'applied'}).nodes().each( function (cell, i) {
                    cell.innerHTML = i+1;
                } );
            } ).draw();
            $('#search').keyup(function () {
                oTable.search($(this).val()).draw();
            });
            oTable.draw();
        });
